<p>Гаражные ворота «Алютех» широко используются при строительстве коттеджей, гаражей и паркингов. Достоинствами секционных ворот являются: удобство и простота использования, надежность, безопасность, хорошая герметизация проема, отличная теплоизоляция.</p>



<p>Полотно секционных ворот  состоит из нескольких секций, подвижно соединенных между собой с помощью стальных петель. Двигаясь по  направляющим, полотно ворот переходит из вертикального (закрытого) положения в  горизонтальное (открытое), уходя под потолок. Такая траектория движения полотна  ворот позволяет <b>сохранить размеры проема</b>, <b>экономит пространство перед воротами и внутри гаража</b>.</p>



<p>Гаражные ворота «Алютех» обеспечивают безопасную эксплуатацию для всех членов семьи. Ворота соответствуют директивам Европейского союза в области безопасности продукции, что подтверждено протоколами испытаний. Наличие протоколов позволяет наносить на гаражные ворота «Алютех» маркировку СЕ.</p>



<p>Использование энергоэффективных панелей и уплотнение по всему периметру ворот обеспечивает комфортную температуру внутри гаража.</p>


