<p>Секционные ворота можно без труда открывать и закрывать вручную, так как их вес скомпенсирован пружинным механизмом. Для подъема гаражных ворот «Алютех» требуется приложить усилие не более 150Н (&asymp; 15 кг).</p>

<p> Гаражные ворота, оснащенные электроприводом, обеспечат дополнительный комфорт в управлении воротами. Автоматизированные гаражные ворота c дистанционным управлением легко и удобно управляются прямо из автомобиля.  Это особенно удобно в плохую погоду &mdash; в дождь или снег. Чтобы открыть ворота, необходимо всего лишь нажать кнопку на пульте дистанционного управления, и ворота открыты.</p>

