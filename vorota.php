        <h1>Секционные (гаражные и промышленные) ворота</h1>
      
          <p>Гаражные и промышленные секционные ворота наиболее популярны по всему миру. 
          Компания OpenGATE специализируется в установке <b>гаражных ворот в Черновцах</b>.
          Такие ворота производят из секций или панелей. 
          Эти секции обладают теплоизолирующим свойством. 
          Обычно, подобная секция состоит из двух листов оцинкованной стали и заполнена пенополиуретаном. 
          Секционные ворота это оптимальное решение из всех видов ворот. 
          Они не занимают полезное место внутри гаража и перед гаражом. 
          Оборудованные автоматикой с дистанционным управлением, секционные ворота могут управляться непосредственно из авто, чем создают дополнительный комфорт. 
          Узнайте больше про <b>секционные ворота в Черновцах</b> (<a href="http://opengate.com.ua/primenenie.garazhnie-vorota.php" class="link">гаражные</a> либо <a href="http://opengate.com.ua/opisanie.promishlenie-vorota.php" class="link">промышленные</a>.) Также воспользуйтесь на странице калькулятора удобной схемой <a href="http://opengate.com.ua/calculator.php" class="link">расчета стоимости гаражных ворот</a> в Черновцах.
          </p>
