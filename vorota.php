        <h1>Распашные ворота</h1>
      
          <p>Традиционный и самый известный вид ворот на Буковине - распашные ворота. 
          Издавна они пользовались популярностью благодаря простоте и очевидности конструкции. 
          При использовании современных электроприводов <b>распашные ворота</b> открываются автоматически. 
          Автоматические приводы могут быть установлены квалифицированными монтажниками OpenGATE на уже существующие конструкции ворот. 
          Функционально распашные ворота состоят из стоек, двух створок и облицовки. 
          Сегодня <a href="http://opengate.com.ua/opisanie.raspashnie-vorota.php" class="link"><b>распашные ворота в Черновцах</b></a> применяются в тех случаях, 
          когда нет возможности использовать откатную конструкцию или же по эстетическим соображениям, 
          либо же при необходимости соотвествовать дизайну шикарных усадеб - ковка, дерево и прочее. 
          Калитки могут варьироваться по цвету, размеру, запорным устройствам и фактуре.</p>
        <br><br>
        
        <h1>Откатные ворота</h1>
      
          <p>Откатные ворота автоматические (их называют еще сдвижные, раздвижные, отьездные) 
          предназначены для усадеб, дач, коттеджных участков, промышленных территорий и придомовых территорий. 
          Автоматические <a href="http://opengate.com.ua/opisanie.otkatnie-vorota.php" class="link"><b>откатные ворота в Черновцах</b></a> такого типа на сегодня самые популярные, удобные, практичные 
          и обладают наилучшей функциональностью. 
          Их конструкция - консольная: когда полотно находится в открытом положении, 
          то ни вверху, ни внизу проема нет никаких препятствий для проезда транспорта. 
          Полотно движется вдоль забора или стены и не занимает пространство ни спереди ни сзади 
          проема в их открытом положении, по сравнению с распашной конструкцией.</p>
